function decodeEntities(value) {
  return value
    .replaceAll('&amp;', '&')
    .replaceAll('&middot;', '·')
    .replaceAll('&mdash;', '—')
    .replaceAll('&ndash;', '–')
    .replaceAll('&copy;', '©')
    .replaceAll('&rarr;', '→')
    .replaceAll('&#39;', "'")
    .replaceAll('&quot;', '"');
}

function translateHtml(html, translations, file) {
  const dictionary = { ...common, ...translations };
  let rawTag = null;
  const pieces = html.split(/(<[^>]+>)/g).map((piece) => {
    if (piece.startsWith('<')) {
      const open = piece.match(/^<(script|style)\b/i);
      const close = piece.match(/^<\/(script|style)>/i);
      if (open) rawTag = open[1].toLowerCase();
      if (close && rawTag === close[1].toLowerCase()) rawTag = null;
      return piece;
    }
    if (rawTag || !piece.trim()) return piece;
    const raw = piece.trim();
    const decoded = decodeEntities(raw);
    const translated = dictionary[decoded];
    if (!translated && /[A-Za-z]{3}/.test(decoded) && !/[ぁ-んァ-ン一-龯]/.test(decoded)) {
      if (!untranslated.has(file)) untranslated.set(file, new Set());
      untranslated.get(file).add(decoded);
    }
    return translated ? piece.replace(raw, translated) : piece;
  }).join('');

  return pieces.replace(/\b(alt|aria-label|content)="([^"]*)"/g, (match, attribute, value) => {
    const decoded = decodeEntities(value);
    const translated = dictionary[decoded];
    const isContentToTranslate = attribute === 'content' && !/^(?:text\/html|width=)/.test(decoded);
    if (!translated && (attribute === 'alt' || isContentToTranslate) && /[A-Za-z]{3}/.test(decoded) && !/[ぁ-んァ-ン一-龯]/.test(decoded)) {
      if (!untranslated.has(file)) untranslated.set(file, new Set());
      untranslated.get(file).add(`[${attribute}] ${decoded}`);
    }
    return translated ? `${attribute}="${translated}"` : match;
  });
}

function makeJapanesePage(file) {
  let html = fs.readFileSync(path.join(root, file), 'utf8')
    .replaceAll('â€”', '—')
    .replaceAll('â€“', '–')
    .replaceAll('â†’', '→');

  html = translateHtml(html, translationsByPage[file] || {}, file);
  html = html.replace('<html lang="en">', '<html lang="ja">');
  html = html.replace(
    new RegExp(`<link rel="alternate" hreflang="en" href="${file.replace('.', '\\.')}">`),
    `<link rel="alternate" hreflang="en" href="../${file}">`
  );
  html = html.replace(
    new RegExp(`<link rel="alternate" hreflang="ja" href="ja/${file.replace('.', '\\.')}">`),
    `<link rel="alternate" hreflang="ja" href="${file}">`
  );
  html = html.replace(
    /<a href="ja\/[^"]+" class="language-switch"[^>]*>日本語<\/a>/,
    `<a href="../${file}" class="language-switch" data-language-switch="en" lang="en" hreflang="en" aria-label="View this page in English">EN</a>`
  );
  html = html.replace(/\b(src|href)="(images|css|js|assets|fonts)\//g, '$1="../$2/');
  return html;
}

